package com.example.ki3.testapp1.model;

public class TicketData {

    private String ticketnum;
    private String ticketname;
    private String ticketimage;
    private int ticketRes;
    private String id;
    private String major;
    private String minor;
    private String date;
    private String ticketuse;
    private String ticketCan;

    public String getTicketnum() {
        return ticketnum;
    }

    public void setTicketnum(String ticketnum) {
        this.ticketnum = ticketnum;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMajor() {
        return major;
    }

    public void setMajor(String major) {
        this.major = major;
    }

    public String getMinor() {
        return minor;
    }

    public void setMinor(String minor) {
        this.minor = minor;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTicketname() {
        return ticketname;
    }

    public void setTicketname(String ticketname) {
        this.ticketname = ticketname;
    }

    public String getTicketimage() {
        return ticketimage;
    }

    public void setTicketimage(String ticketimage) {
        this.ticketimage = ticketimage;
    }

    public int getTicketRes() {
        return ticketRes;
    }

    public void setTicketRes(int ticketRes) {
        this.ticketRes = ticketRes;
    }

    public String getTicketuse() {
        return ticketuse;
    }

    public void setTicketuse(String ticketuse) {
        this.ticketuse = ticketuse;
    }

    public String getTicketCan() {
        return ticketCan;
    }

    public void setTicketCan(String ticketCan) {
        this.ticketCan = ticketCan;
    }
}
